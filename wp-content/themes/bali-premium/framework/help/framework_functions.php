    <h2>Les Fonctions</h2>
    <p>Le framework intègre une série de fonction permettant de faire un racrourci par rapport à des opérations fréquentes comme la récupérations de certains infos.</p>
    <p>
        <em>- <strong>get_theme_option(id,name)</strong> Permet de récupérer une option du thème</em><br/>
        &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <strong>id</strong> Id du pacquet contenant l'option (représente le slug du panneau d'option qui a permis la définition de l'option)<br/>
        &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <strong>name</strong> Nom de l'option a récupérer
    </p>
    <p>
        <em>- <strong>get_attachments()</strong> Permet de récupérer la liste des fichiers attaché à un article ou un custom_post_type (à utiliser dans une boucle)</em><br/>
    </p>
